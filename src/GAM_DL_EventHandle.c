#include "GAM_DL.h"

int	GAM_DL_EventHandle(SDL_Event *event)
{
	if (event->type == SDL_EVENT_QUIT)
		return (1);
	if (event->type == SDL_EVENT_KEY_DOWN && event->key.key == SDLK_ESCAPE)
		return (1);
	return (0);
}
